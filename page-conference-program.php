			<li><strong>Organize a booth</strong><br />
				Going to a trade show or conference for ownCloud is not hard. All you need are a few volunteers and some positive energy! Read the steps below to learn how to organize a booth.
				<ul>
					<li><em>Get the booth!</em><br />
						Work out a booth space with the organization of the event.<br />
						Now, get some people together who can help and arrange travel, hotel and all that! Before the event, try to print some of our
						<a target="_blank" href="https://github.com/owncloud/promo">promo materials</a> and see <em>Resources</em> above for ordering some things from us.</li>

					<li><em>Tell everybody about the booth!</em><br />
						Once you've secured a spot, share the ownCloud attendance to the event over social media -
						perhaps even set up an event page on Facebook and Google plus, ask people to share the news. And <a href="/events/contactform">tell us about it</a>!</li>

					<li><em>Set up the booth</em><br />
						A crucial thing when setting up a booth is to create open space. This is very hard, as many events put a row of tables in front of the visitors 
						and it is of course easy to go and sit behind them. But that is hardly an effective strategy. Instead, put the table back against the wall if you 
						can, and stand in front. That is much nicer for visitors! Nobody likes to be the first person at a booth, and if you and your booth friends are 
					<span></span>tanding in front, it seems there is always something going on.<br />

					<li><em>Promote the booth</em><br />
						Get people to the booth! You can put some leaflets or hang up some posters around the venue, and if you give a talk you should certainly 
						mention that there is an ownCloud booth.</li>

					<li><em>Handle visitors</em><br />
						There are four kind of visitors you get at the booth: newbies, advanced users, business users and (potential) contributors. You're there to get 
						newbies interested in ownCloud, answer questions of the advanced users and convince the (potential) business users that their company needs 
						ownCloud! Last but not least, you should convince (potential) contributors to get on board with ownCloud hacking: it is fun and easy.</li>
					</ul>
					<p>To help you talk about ownCloud, we have a <a target="_blank" href="<?php echo get_template_directory_uri(); ?>/assets/files/FAQ.pdf">conference team FAQ</a> and
					some <a target="_blank" href="<?php echo get_template_directory_uri(); ?>/assets/files/TalkingPoints.pdf">conference talking points</a>. Print these pages,
					read them and bring them for the others on the booth to read in a quiet moment. Of course, you can use our flyers, posters and other materials. You
					can <a target="_blank" href="https://github.com/owncloud/promo">download designs here</a> for printing. You can also find our logo there. Please do follow the usage guidelines on that page.</p>
