<h3>Why do you require a contributor agreement now?</h3>
<p>We have set up a company to speed up ownCloud development and pay the bills of ownCloud developers.
We thought a lot about the relationship between the company and community and about a solution that is best for both parties.</p>
<p>The company needs to have a business model so that it can sell software, support and services to big companies.
Many customers are willing to pay for a non-AGPL version of ownCloud, some would not even use ownCloud without a proprietary license.
Aside from the <em>'Enterprise Edition'</em> under a closed license, we of course sell support and other services.
The revenue is used to fund development of ownCloud, marketing, services, sponsoring and so on.</p>
<p>The community wants to be sure that all the contributions and all the hard work stays Free Software.
That is guaranteed by the AGPL license.</p>
<p>And the community wants to make ownCloud better and more successful. That is where the company helps.
ownCloud benefits enormously from the resources that the company invests into it, including coding, marketing and more.
The company pays ownCloud coders to write features the customers need and works with the community to make ownCloud a better product.
Because the company works in the open and as part of the community, and because the code is released under the AGPL,
ownCloud itself continues to be free. Essentially, ownCloud GmbH customers pay for ownCloud contributors writing more AGPL code for ownCloud.
So we think that we found a solution here which is good for all parties.</p>
