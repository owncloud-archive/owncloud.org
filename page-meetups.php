	<p>Want to organize a meetup? Below you will find some practical tips on how to organize a great meetup
	as well as information on how to tell us about your meetup and how to get some ownCloud goodies!</p>

	<h2>Organize</h2>
		<p>All you really need for a meetup are a room with power and wifi, some content and interested visitors!</p>

		<h3>Where and when?</h3>
		<p>You can often get a room at work, university, community center, local hacker space, hacker caf√© or a public library.</p>

		<p>We do many ownCloud Meetups around 19:00 on week days, but if that does not work for you, try a weekend! And of course: just ask the participants 
			if another time would work better.</p>

		<h3>Tell everybody!</h3>
		<p>We use <a href="http://meetup.com">meetup.com</a> to track and promote most of our events. We have an account where we can set up Meetups, so if you're up for
			organizing one, let us know and we get you going. Here is an <a href="http://www.meetup.com/owncloud-berlin/">example page</a>.</p>

		<p>Next up is to share it over social media - set up an event page on Facebook and Google plus, ask people to share the news. Don't feel bad if your first
			meeting attracts just 5 visitors and don't freak out if 30 RSVP. A small group means you each have time to really work together; and a big group 
			means everybody will talk to everybody so you don't have to worry about keeping everybody busy!</p>

		<h3>What to do at the event?</h3>
		<p>Now you have a place and some participants, but what to do? It is easiest to separate two kinds of Meetups: the one where (potential) ownCloud 
			contributors come together; and one where users meet up.</p>
		<p><strong>Development Meetups</strong> need very little: room, wifi, people with laptops. Keep it simple: ownClouders will get along just fine. Open by doing
			a round of introductions, ask everybody to share what they would like to do. You can use one or more of our <a href="http://github.com/owncloud/promo">ready-made workshop presentations to get started</a> but if everybody just wants to get hacking: that's great!</p>

		<p>Of course, every now and then you could get an interesting speaker to discuss some Cloudy subject.</p>
	<img src="<?php echo get_template_directory_uri(); ?>/assets/img/events/meetup2.jpg" style="width: 400px" title="pic by Raghu Nayar" alt="pic by Raghu Nayar" class="img-thumbnail alignright">

		<p><strong>User Meetups</strong> require a little more work. It is usually best to, after introductions, have a presentation or demonstration - use a
			<a href="https://github.com/owncloud/promo/tree/master/Presentation%20materials">ready-made workshop or  presentation to get started</a>.
			After that, have room for questions and discussions. Be sure to announce the subject beforehand so people know what to expect,
			and solicit input: ideas, requests or offers for help. We are working to offer content for a number of meetings
			but it is also nice to encourage members of the meetup group to contribute their own knowledge and experience!</p>

		<h3>Tips</h3>
		<ul>
		    <li>It is nice to be welcome, so welcome newcomers! If your meetup gets big, you can ask somebody to help you welcoming new visitors...</li>
		    <li>get people to sign up on meeetup.com or ask for their mail addresses so you can tell them about the next meetup!</li>
		    <li>Find people to help you. Try to organize the meetup with others so when you're on holiday, the event can still take place.</li>
		    <li>Remind people of the ownCloud code of conduct if they aren't nice to each other: you have every right to tell them to stay away if they can't behave... It is your meetup!</li>
		    <li>Find a <a href="http://blog.jospoortvliet.com/2014/11/5-steps-to-organizing-meetup.html">how to organize a meetup in 5 steps</a> article here.</li>
		</ul>
